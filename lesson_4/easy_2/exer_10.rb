# What are the benefits of Object Oriented Programming?

# - It makes it easier to maintain larger, more complex programs.
# - With encapsulation, it only makes a certain number of methods available from
# each class while the others are contained in the class
# - With classes interacting with just a few methods, it decreases dependency and
# - protects data within the class
# - It reduces repetition
# - OOP gives us more flexibility in using pre-written code for new purposes.
# - It allows the programmer to think on a new level of abstraction by creating
# objects which are represented by nouns and methods which can act on the objects.
# - Inheritance allows programmers to define large classes with basic method and
# large reusability and smaller sub-classes that are more finely tuned. 

